            <h4>element.style</h4>

            <p>
                La propriété style d'une balise permet de lire et modifier les styles CSS directement à partir de JavaScript. Les propriétés qui ont habituellement des traits-d'union en css sont en camelcase. Par exemple: margin-left en CSS = marginLeft en JS
                <br>
                <strong>element.<span class="specification">style.color</span> = ("rgb(255, 0, 0)")</strong>
                <br> 
                <strong>element.<span class="specification">style.backgroundColor</span> = ("#000")</strong>
                <br> 
                <strong>element.<span class="specification">style.marginLeft</span> = ("25px")</strong>
            </p>

