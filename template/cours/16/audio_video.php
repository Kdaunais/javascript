<div class="d-grid">

    <div class="grid-item">

        <h4>Audio</h4>
        <p>
            L'élément HTML <span class="specification">Audio</span> est utilisé pour lire un fichier audio sur une page Web.
            <br>
            <br>
            Jouer l'audio: 
            <br>
            <strong>element.<span class="specification">play()</span></strong>
            <br>
            Mettre l'audio en pause: 
            <br>
            <strong>element.<span class="specification">pause()</span></strong>
            <br>
            Arrêter l'audio: 
            <br>
            <strong>element.<span class="specification">pause()</span></strong>
            <br>
            <strong>element.<span class="specification">currentTime = 0</span></strong>
            <br>
            Recommencer l'audio: 
            <br>
            <strong>element.<span class="specification">currentTime = 0</span></strong>
        </p>

